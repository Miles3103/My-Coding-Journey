#include <stdio.h>

int main(void)
{
    // 1. Initialize i to 1
    // 2. Check if i <= 5
    // 3. Run code, then increment i++
    for (int i = 1; i <= 5; i++)
    {
        printf("Iteration number: %d\n", i);
    }

    return (0);
}
