#include <stdio.h>

int main(void)
{
    int a = 10, b = 3;

    printf("Addition:       %d\n", a + b);  // 13
    printf("Subtraction:    %d\n", a - b);  // 7
    printf("Multiplication: %d\n", a * b);  // 30
    printf("Division:       %d\n", a / b);  // 3 (integer division)
    printf("Modulus:        %d\n", a % b);  // 1 (remainder)

    return (0);
}
